    private void complainMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_complainMouseReleased
        String comtype=(String) ctype.getSelectedItem();
        String comloc = cloc.getText().trim();
        if(!comloc.equals("") && !comtype.equals("Select Complain Type")){
            try {
                //inserting to DB
                db.DB.complain.setString(1, comtype);
                db.DB.complain.setString(2, cid.getText());
                db.DB.complain.setString(3, cn.getText());
                db.DB.complain.setString(4, comloc);
                db.DB.complain.setString(5, cremarks.getText());
                db.DB.complain.setString(6, "Waiting For Approval");
                db.DB.complain.executeUpdate();
                
                ResultSet rs = db.DB.getComID.executeQuery();
                rs.next();
                comID.setText(String.valueOf(rs.getInt(1)));
                
                CardLayout cd = (CardLayout) jPanel6.getLayout();
                cd.show(jPanel6, "comReg");
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please provide Complain Type and Location");
        }
    }//GEN-LAST:event_complainMouseReleased

