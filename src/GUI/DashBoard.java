    private void t1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_t1ActionPerformed
        int searchId = Integer.parseInt(t1.getText());
        
        try {
            //fetching complain details
            db.DB.searchC.setInt(1, searchId);
            ResultSet rs = db.DB.searchC.executeQuery();
            
            if(rs.next()){
                if(rs.getString("sid").equals(STID)){
                    cid2.setText(t1.getText());
                    ctype2.setText(rs.getString("ctype"));
                    cloc2.setText(rs.getString("loc"));
                    cremarks2.setText(rs.getString("remarks"));
                    st.setText(rs.getString("status"));
                }else{
                    JOptionPane.showMessageDialog(null, "Complain ID does not belong to your Account!");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Complain ID does not Exist!!");
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_t1ActionPerformed

